// Translate a UI string for a locale, falling back to English.
export function ct(locale, key) {
  const dict = STRINGS[locale] || STRINGS.en
  return dict[key] ?? STRINGS.en[key] ?? key
}

// Text of one anamnesis question in the given locale.
export function questionText(locale, id) {
  const dict = STRINGS[locale] || STRINGS.en
  return dict.q?.[id] ?? STRINGS.en.q[id] ?? id
}

// English question text — used server-side so the summarizing model always
// knows what was asked, regardless of the patient's language.
export function questionTextEn(id) {
  return STRINGS.en.q[id] ?? id
}
