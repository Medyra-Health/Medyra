const originalPrices = { free: '', onetime: '€4.99', personal: '€18', family: '€38', clinic: '' }
const allLangs = ['en', ...Object.keys(fixes)]

allLangs.forEach(lang => {
  const filePath = path.join(messagesDir, lang + '.json')
  const data = JSON.parse(fs.readFileSync(filePath, 'utf8'))
  const vals = lang === 'en' ? enExtras : fixes[lang]

  // Add all pricing keys
  Object.assign(data.pricing, {
    launchBadge: vals.launchBadge,
    launchNote: vals.launchNote,
    comingSoon: vals.comingSoon,
    goToDashboard: vals.goToDashboard,
    faqTitle: vals.faqTitle,
    launchOfferBanner: vals.launchOfferBanner,
    limitedOffer: vals.limitedOffer,
    fiftyOff: vals.fiftyOff,
    applyCode: vals.applyCode,
    copied: vals.copied,
    copyCode: vals.copyCode,
    pasteCode: vals.pasteCode,
  })

  // Add originalPrice to all tiers
  Object.entries(originalPrices).forEach(([tier, price]) => {
    if (data.pricing.tiers[tier]) {
      data.pricing.tiers[tier].originalPrice = price
    }
  })

  fs.writeFileSync(filePath, JSON.stringify(data, null, 2))
  console.log('✅ Updated ' + lang + '.json')
})

