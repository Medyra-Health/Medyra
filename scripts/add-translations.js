// Apply new keys to each language file
const langCodes = Object.keys(newKeys)

langCodes.forEach(lang => {
  const filePath = path.join(messagesDir, `${lang}.json`)
  if (!fs.existsSync(filePath)) {
    console.log(`SKIP: ${lang}.json not found`)
    return
  }

  const data = JSON.parse(fs.readFileSync(filePath, 'utf8'))
  const additions = newKeys[lang]

  // Merge each section
  Object.entries(additions).forEach(([section, keys]) => {
    if (!data[section]) data[section] = {}
    Object.assign(data[section], keys)
  })

  fs.writeFileSync(filePath, JSON.stringify(data, null, 2), 'utf8')
  console.log(`✅ Updated ${lang}.json`)
})

console.log('\nDone! All language files updated.')
